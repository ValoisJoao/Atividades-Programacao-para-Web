let num_A = 10;
let num_B = 5;

// Soma
let resultadoSoma = num_A + num_B;
console.log(`Resultado da soma entre ${num_A} e ${num_B} é ${resultadoSoma}`);

// Subtração
let resultadoSub = num_A - num_B;
console.log(`Resultado da subtração entre ${num_A} e ${num_B} é ${resultadoSub}`);

// Multiplicação
let resultadoMult = num_A * num_B;
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} é ${resultadoMult}`);

// Divisão
let resultadoDiv = num_A / num_B;
console.log(`Resultado da divisão entre ${num_A} e ${num_B} é ${resultadoDiv}`);